Obstetrica este o ramura a medicinei care se ocupa cu supravegherea femeii in perioada de sarcina,
nastere si postpartum. Serviciile de obstetrica furnizate in Centrul Medical de Diagnostic si Tratament
"Dr. Ina Jacote" cuprind diagnosticarea sarcinii si monitorizarea clinica, de laborator si ecografica
a sarcinii, investigarea cauzelor de infertilitate si depistarea la timp a anomaliilor cromozomiale.
In aceasta directie in centru se pot efectua analize de laborator specifice
(Determinari hormonale & Fertilitate, Bacteriologie & Serologie antibacteriana, Virusologie & Serologie antivirala)
si investigatii ecografice de ovulatie si de sarcina.</div>
